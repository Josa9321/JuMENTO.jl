function test_simple_problems(model, saved_frontier_set; is_augmecon_2=false)
    set_attribute(model, MethodAUGMECON.Bypass(), is_augmecon_2)

    optimize!(model)

    frontier_set = hcat(
        [objective_value(model, result=i
        ) for i in Base.OneTo(result_count(model))]...)
    frontier_set = frontier_set[:, sortperm(frontier_set[1, :])]

    @test all(isapprox.(frontier_set, saved_frontier_set))
    return nothing
end

function test_no_solution_problems(model, reason; is_augmecon_2)
    set_attribute(model, MethodAUGMECON.Bypass(), is_augmecon_2)
    optimize!(model)
    @test JuMP.termination_status(model) == reason
    return nothing
end

